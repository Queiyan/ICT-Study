package ch01;

// 클래스명은 대문자로 시작한다.
public class Hellojava {
	public static void main(String[] args) {
		/*
		 * 프로그램 시작하는 main() 메서드는 소문자로 시작한다.
		 * 자바가상머신(Java Virtual Machine, JVM)이 프로그램을 시작하기 위해 호출하는 메서드
		 */
		System.out.println("Hello, Java");
		System.out.println("아자 아자 화이팅!");
	}

}
